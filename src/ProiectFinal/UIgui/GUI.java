    private void bAdaugaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAdaugaActionPerformed

        try {
            String nume = jTextField1.getText();
            String prenume = jTextField2.getText();
            String data = jTextField3.getText();
            String telefon = jTextField4.getText();
            
            a.Adauga(nume, prenume, data, telefon);
            l.setModel(model);
            actualizeazaModel(a.Contacte);
            Adaugare_contacte.setVisible(false);
            clearForm();
        } catch (RuntimeException e) {
            if (e.getMessage().equals("NUMELE SAU PRENUMELE PREA SCURT!")) {
                JOptionPane.showMessageDialog(this, "NUMELE SAU PRENUMELE PREA SCURT!");
            }
            if (e.getMessage().equals("contactul exista!")) {
                JOptionPane.showMessageDialog(this, "CONTACTUL EXISTA DEJA IN LISTA DE CONTACTE!");
            }
            if (e.getMessage().contains("parse")) {
                JOptionPane.showMessageDialog(this, "PROBLEMA LA FORMATUL DATEI zz/ll/yyyy!");
            }
            if (e.getMessage().contains("Numaru nu are 10 cifre...")) {
                JOptionPane.showMessageDialog(this, "NUMARUL NU ARE 10 CIFRE!");
            }
            if (e.getMessage().contains("Numarul nu incepe cu 07,02 sau 03!")) {
                JOptionPane.showMessageDialog(this, "NUMARUL DE TELEFON NU INCEPE CU 07,02 sau 03!");
            }
        }
    }//GEN-LAST:event_bAdaugaActionPerformed

