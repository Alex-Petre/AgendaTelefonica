    private void mbDeschidereMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mbDeschidereMouseClicked
        int val = fc.showOpenDialog(this);
        if (val == JFileChooser.APPROVE_OPTION) {
            File f = fc.getSelectedFile();
            Timer t = new Timer();
            TimerTask task = new TimerTask() {
                @Override
                public void run() {

                    try {
                        try (FileInputStream fis = new FileInputStream(f)) {
                            ObjectInputStream ois = new ObjectInputStream(fis);
                            List contacte;

                            contacte = (List) ois.readObject();
                            l.setModel(model);
                            a.Contacte = contacte;
                            actualizeazaModel(contacte);

                            ois.close();
                            fis.close();

                        }
                    } catch (IOException ex) {
                        Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            };
            t.schedule(task, 0, 30000);
        }
    }//GEN-LAST:event_mbDeschidereMouseClicked

