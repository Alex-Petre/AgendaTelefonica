    private void bFiltreazaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFiltreazaActionPerformed
        // TODO add your handling code here:
        int selectedIndex = cbf.getSelectedIndex();

        if (selectedIndex == 0) { //dupa numar fix;
            a.filtreazaNrFix();
            List contacte_filtrate = a.contacte_functie(true);
            actualizeazaModel(contacte_filtrate);
        }

        if (selectedIndex == 1) { //dupa numar mobil;
            a.filtreazaNrMobil();
            List contacte_filtrate = a.contacte_functie(true);
            actualizeazaModel(contacte_filtrate);
        }

        if (selectedIndex == 2) { //dupa nascuti in ziua asta;
            a.filtreazaNascutiAstazi();
            List contacte_filtrate = a.contacte_functie(true);
            actualizeazaModel(contacte_filtrate);
        }

        if (selectedIndex == 3) { //dupa numar luna;
            a.filtreazaNascutiLunaCurenta();
            List contacte_filtrate = a.contacte_functie(true);
            actualizeazaModel(contacte_filtrate);
        }

        if (selectedIndex == 4) { //personalizat
            filtru = tf2.getText();
            a.filtreazaPersonalizat(filtru);
            List contacte_filtrate = a.contacte_functie(true);
            actualizeazaModel(contacte_filtrate);
    }//GEN-LAST:event_bFiltreazaActionPerformed
    }

